package stonegame.model;

/**
 * The {@code Square} enum represents the possible states of a square on the game board.
 * It can be either empty ({@code NONE}), occupied by player 1 ({@code PLAYER1}), or occupied by player 2 ({@code PLAYER2}).
 */
public enum Square {
    /** Represents an empty square on the game board. */
    NONE,
    /** Represents a square occupied by player 1. */
    PLAYER1,
    /** Represents a square occupied by player 2. */
    PLAYER2,
}
